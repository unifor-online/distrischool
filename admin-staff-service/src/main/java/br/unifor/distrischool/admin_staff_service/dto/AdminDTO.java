package br.unifor.distrischool.admin_staff_service.dto;

public class AdminDTO {

    private Long id;
    private String name;
    private String email;
    private String role;
    private String password; // Usado apenas para criação/atualização. Não deve ser retornado em respostas GET.

    public AdminDTO() {
    }

    public AdminDTO(Long id, String name, String email, String role) {
        this.id = id;
        this.name = name;
        this.email = email;
        this.role = role;
    }

    // Getters e Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}